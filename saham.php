        <h1>SAHAM</h1>
        <br>
        <h3>Defenisi</h3>
        <p>Saham dapat diartikan sebagai tanda penyertaan modal seseorang atau pihak (badan usaha) pada suatu perusahaan atau Perseroan Terbatas. 
            Dengan menyertakan modal tersebut, maka pihak tersebut memiliki klaim (hak) atas pendapatan perusahaan, aset perusahaan, dan berhak hadir dalam Rapat Umum Pemegang Saham (RUPS).</p>
            <p>Di pasar sekunder (bursa) atau dalam aktivitas perdagangan saham sehari-hari, harga-harga saham mengalami fluktuasi baik berupa kenaikan maupun penurunan. Pembentukan harga saham terjadi karena adanya permintaan dan penawaran atas saham tersebut. </p>
            <p>Permintaan dan penawaran atas suatu dipengaruhi banyak faktor, baik yang sifatnya spesifik berhubungan saham tersebut (kinerja perusahaan dan industri dimana perusahaan tersebut berada) maupun faktor yang sifatnya makro atau eksternal, seperti perkembangan tingkat suku bunga, inflasi, nilai tukar, dan faktor-faktor non ekonomi seperti kondisi sosial dan politik.
            </p>
            <h3>Keuntungan Memiliki Saham</h3>
            <ul>
                <li>
                    <h4>Mendapatkan <i>dividen</i></i></h4>
                    <p>Dividen adalah bagian keuntungan perusahaan yang dibagikan kepada pemegang saham. Jumlah dividen yang akan dibagikan diusulkan oleh Dewan Direksi perusahaan dan disetujui di dalam Rapat Umum Pemegang Saham (RUPS).</p>
                </li>
                <li>
                    <h4><i>Capital Gain</i></h4>
                    <p>Capital Gain adalah keuntungan ketika investor menjual saham dengan harga yang lebih tinggi dari harga belinya. Saham merupakan aset yang likuid, jadi mudah untuk diperjualbelikan (via Bursa)</p>
                </li>
            </ul>
            <h3><u>Risiko Memiliki Saham</u></h3>
            <ul>
                <li>
                    <h4>Tidak Mendapatkan <i>Dividen</i></h4>
                    <p>Umumnya perusahaan membagi dividen ketika perusahaan menunjukkan kinerja yang baik. Namun ketika perusahaan mengalami penurunan kinerja atau merugi maka perusahaan tidak dapat membagikan dividen</p>
                </li>
                <li><h4><i>Capital Loss</i></h4>
                    <p>Capital Loss merupakan kebalikan Capital Gain. Hal ini terjadi jika kita menjual saham yang kita miliki lebih rendah dari harga beli.</p>
                </li>
                <li>
                    <h4>Risiko Likuidasi</h4>
                    <p>Jika Emiten bangkrut atau dilikuidasi, para pemegang saham memiliki hak klaim terakhir terhadap aktiva perusahaan setelah seluruh kewajiban Emiten dibayarkan. Kemungkinan terburuknya adalah jika tidak lagi aktiva yang tersisa, maka pemegang saham tidak akan memperoleh apa-apa.</p>
                </li>
            </ul>
            <h3><u>Saham di-<i>delisting</i> dari Bursa Efek</u></h3>
            <p>Karena beberapa alasan tertentu, saham dapat dihapus pencatatannya dari Bursa (delisting) sehingga pada akhirnya saham tersebut tidak dapat diperdagangkan (menjadi tidak likuid).</p>
            <h3>Membeli Saham</h3>
            <p>Investor dapat membeli saham suatu Emiten di Pasar Modal, melalui 2 cara:</p>
            <ul>
                <li>
                    <p></p>Membeli di Pasar Perdana, yaitu pada saat saham ditawarkan pertama kalinya kepada msayarakat/investor (yang lazim disebut Penawaran Umum Saham Perdana atau IPO atau go public).</p>
                </li>
                <li><p>Membeli di Pasar Sekunder, yaitu membeli saham yang dimiliki investor lainnya melalui Perusahaan Efek (broker) yang menjadi Anggota Bursa (AB). Hanya Perusahaan Efek yang menjadi Anggota Bursa (AB) yang dapat melakukan jual-beli saham melalui sistem perdagangan di Bursa Efek Indonesia (Jakarta Automated Trading System/JATS).</p>
                </li>
            </ul>
            <h3>Bagaimana Investor dapat membeli Saham melalui Perusahaan Efek ?</h3>
            <p>Investor dapat membeli Saham melalui Perusahaan Efek dengan cara membuka Rekening pada Perusahaan Efek dengan persyaratan secara umum sebegai berikut:</p>
            <ul>
                <li>
                    <p>Mengisi formulir dan menyerahkan foto copy KTP yang berlaku.</p>
                </li>
                <li>
                    <p>Mengisi formulir yang telah disediakan oleh pihak Perusahaan Efek dan mengisi formulir Prinsip Mengenal Nasabah (<i>Know Your Client Principle</i>). Ketentuan tersebut termasuk menyampaikan NPWP (Nomor Pokok Wajib Pajak).</p>
                </li>
                <li>
                    <p>Membuka rekening di Bank sesuai ketentuan Perusahaan Efek bersangkutan dan menyimpan sejumlah uang sebagai deposit awal.</p>
                </li>
                <li>
                    <p>Masing-masing Perusahaan Efek (broker) memiliki ketentuan minimal uang deposit berbeda-beda. (rata-rata sekitar Rp. 5 Juta atau lebih)</p>
                </li>
                <li>
                    <p>Setelah disetujui, investor sudah siap bertransaksi.</p>
                </li>
            </ul>
            <h3>Apa itu AKSes (Acuan Kepemilikan Sekuritas)?</h3>
            <p>Pada tanggal 18 Juni 2009, PT Kustodian Sentral Efek Indonesia (KSEI) meluncurkan fasilitas AKSes yang memungkinkan para investor pasar modal Indonesia untuk memonitor data posisi kepemilikan Efek dan/atau dana melalui jaringan internet. 
                Fasilitas ini diharapkan dapat memberikan keterbukaan informasi kepada nasabah sehingga nasabah dapat terlibat langsung dengan memonitor catatan kekayaannya.
                </p>
